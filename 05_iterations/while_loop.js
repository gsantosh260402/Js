// while loop

let i = 1;

while (i<=5 ) {
    console.log(`value is : ${i}`);
    i++;
}


myArray = ["flash" , "batman" , "superman"];
let index = 0;
while(index < myArray.length){
    console.log(`value is : ${myArray[index]}`);
    index++;
}


// do-while loop

let score = 7;

do{
   console.log(`value is : ${score}`);
   score++;
}while(score<=5);
